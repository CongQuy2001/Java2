    public void filltable() {
        mode.setRowCount(0);
        for (SinhVien i : SV) {
            Object sv[] = new Object[5];
            sv[0] = i.getMa();

            sv[1] = i.getHoten();

            sv[2] = i.getLuong();

            sv[3] = i.getEmail();

            sv[4] = i.getTuoi();

            mode.addRow(sv);

        }
        mode.fireTableDataChanged();
    }

