    public void check(int D) {
        jTextNAME.setText(SV.get(D).getHoten());

        jTextYEAR_OLD.setText(String.valueOf(SV.get(D).getTuoi()));
        jTextEMAIL.setText(SV.get(D).getEmail());
        jTextSALARY.setText(String.valueOf(SV.get(D).getLuong()));
        jTextID.setText(String.valueOf(SV.get(D).getMa()));
        jTable1.setRowSelectionInterval(D, D);
        jLabel8.setText(
                "Record " + String.valueOf(D + 1) + " of " + String.valueOf(SV.size()));
    }

    public boolean checkluong() throws HeadlessException {
        try {
            double luong = Double.parseDouble(jTextSALARY.getText());
            if (luong < 5) {
                return false;
            } else {
                return true;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nhập sai định dạng lương", "LỖI", JOptionPane.ERROR_MESSAGE);
            return false;
        }

    }

    public void remode() {
        int index = jTable1.getSelectedRow();
        SV.remove(index);
    }

