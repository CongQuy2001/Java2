    private void BNTSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BNTSAVEActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if (jTextID.getText().trim().length() <= 0) {
            

            sb.append("Mã không được để trống");
        }
      
        if (jTextNAME.getText().trim().length() <= 0) {
            sb.append("Tên không được để trống");
        }
        if (jTextYEAR_OLD.getText().isEmpty()) {
            sb.append("Tuổi không được để trống");
        }
        if (jTextEMAIL.getText().isEmpty()) {
            sb.append("email không được để trống");
        }
        if (jTextSALARY.getText().isEmpty()) {
            sb.append("Lương không được để trống");
        }
        if (sb.length() > 0) {
            JOptionPane.showConfirmDialog(rootPane, sb.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (checkluong() == true && checkMail() == true) {
            add();
            filltable();
        } else {
            JOptionPane.showMessageDialog(null, "email hoặc lương không hợp lệ");
        }


    }//GEN-LAST:event_BNTSAVEActionPerformed

    public boolean checkMail() {
        String Email = "\\w+@\\w+\\.\\w+";
        if (!jTextEMAIL.getText().matches(Email)) {

            return false;
        } else {
            return true;
        }
    }

